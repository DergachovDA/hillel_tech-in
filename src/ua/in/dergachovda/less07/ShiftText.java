package ua.in.dergachovda.less07;

public class ShiftText {

    public static void main(String[] args) {

        byte number = 16;

        number <<= 1;

        number >>= 2;

        System.out.println("number = " + number);

        int test = 13;
        test <<= 1;
        System.out.println("test = " + test);

    }

}
